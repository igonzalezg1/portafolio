// ─── Types ────────────────────────────────────────────────────────────────────

export interface FileNode {
  name: string
  type: 'file' | 'dir'
  path: string
  children?: FileNode[]
}

export interface FileData {
  code: string[]
  previewType: PreviewType
  previewData: PreviewData
}

export type PreviewType =
  | 'inicio'
  | 'experiencia'
  | 'proyecto'
  | 'habilidades'
  | 'certificaciones'
  | 'contacto'

export type PreviewData =
  | InicioData
  | ExperienciaData
  | ProyectoData
  | HabilidadesData
  | CertificacionesData
  | ContactoData

export interface InicioData {
  nombre: string
  rol: string
  ubicacion: string
  imagen?: string
  estadisticas: Array<{ valor: string; etiqueta: string }>
  proyectos: Array<{ nombre: string; descripcion: string }>
  abierto: string[]
}

export interface ExperienciaData {
  empresa: string
  año: string
  cargo: string
  descripcion: string
  stack: string[]
}

export interface ProyectoData {
  nombre: string
  empresa: string
  año: string
  descripcion: string
  stack: string[]
  imagen: string
}

export interface HabilidadesData {
  categorias: Array<{ nombre: string; items: string[]; iconos: string[] }>
}

export interface CertificacionesData {
  educacion: Array<{ titulo: string; institucion: string; fecha: string }>
  cursos: Array<{ titulo: string; plataforma: string; fecha: string }>
}

export interface ContactoData {
  email: string
  telefono: string
  linkedin: string
  ubicacion: string
  disponibilidad: string[]
}

